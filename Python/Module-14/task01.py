# Вы получили файл spaceships.csv с информацией о космических кораблях.
# Каждая строка представляет собой запись о конкретном космическом
# корабле, а столбцы содержат следующую информацию: "Название", "Тип",
# "Год запуска", "Максимальная скорость (км/ч)", "Страна производитель".
# Напишите программу, которая считывает информацию из данного файла.
# В первой строке требуется вывести общее количество кораблей в файле.
# Во второй строке выведите сколько кораблей было запущено после 2000
# года. В третьей строке через пробел выведите название и страну
# производителя корабля с максимальной скоростью.

import csv

def analyze_spaceships(filename):
    with open(filename, 'r', encoding='utf-8') as file:
        reader = csv.DictReader(file)
        data = list(reader)
        
    total = len(data)
    after_2000 = sum(1 for row in data if int(row['Год запуска']) > 2000)
    fastest = max(data, key=lambda x: int(x['Максимальная скорость (км/ч)']))
    
    print(total)
    print(after_2000)
    print(fastest['Название'], fastest['Страна производитель'])

analyze_spaceships('spaceships.csv')
